// Cache for translatable elements to improve performance
let translatableElementsCache = null;

/**
 * Localization Logic – v3.0 (Liquid Fast)
 * Optimized for high performance across all pages including wings.
 */
function setLanguage(lang) {
  const map = langMap[lang];
  if (!map) return;

  // Always refresh cache to get newly injected elements
  translatableElementsCache = Array.from(document.querySelectorAll('[data-langkey]'));

  // Use requestAnimationFrame for smoother UI update
  requestAnimationFrame(() => {
    translatableElementsCache.forEach(el => {
      const key = el.getAttribute('data-langkey');
      const text = map[key];
      if (!text) {
        console.warn(`Missing translation for key: ${key}`);
        return;
      }

      const tag = el.tagName;

      // inputs & textareas
      if (tag === 'INPUT' || tag === 'TEXTAREA') {
        if (el.type === 'submit' || el.type === 'button') {
          if (el.value !== text) el.value = text;
        } else {
          if (el.placeholder !== text) el.placeholder = text;
        }
        return;
      }

      // <option> tags
      if (tag === 'OPTION') {
        if (el.textContent !== text) el.textContent = text;
        return;
      }

      // Complex elements with icons or HTML
      if (text.includes('<')) {
        if (el.innerHTML !== text) el.innerHTML = text;
        return;
      }

      // Preserve Icons while updating text (highly optimized)
      if (el.children.length > 0) {
        const icon = el.querySelector('i, svg');
        if (icon) {
          let replaced = false;
          for (const node of el.childNodes) {
            if (node.nodeType === Node.TEXT_NODE) {
              const trimmed = node.textContent.trim();
              if (!replaced && trimmed.length > 0) {
                node.textContent = ' ' + text + ' ';
                replaced = true;
              } else if (replaced) {
                node.textContent = '';
              }
            }
          }
          if (!replaced) el.appendChild(document.createTextNode(' ' + text));
          return;
        }
      }

      // Default: Update textContent (fastest)
      if (el.textContent !== text) {
        el.textContent = text;
      }
    });

    // Update Title & Meta
    if (map.site_title) document.title = map.site_title;
    const meta = document.querySelector('meta[name="description"]');
    if (meta && map.site_desc) meta.setAttribute('content', map.site_desc);
    
    document.documentElement.lang = lang;
  });
}

/**
 * Initialize Components
 */
function initComps() {
  const h = document.getElementById('site-header');
  const f = document.getElementById('site-footer');

  // Reset cache as new elements are injected
  translatableElementsCache = null;

  // Inject header & footer HTML first
  if (h) h.innerHTML = HEADER_HTML;
  if (f) f.innerHTML = FOOTER_HTML;

  // Read saved preference (default: Hindi)
  const savedLang = localStorage.getItem('aimhop-lang') || 'hi';

  // Apply language to the entire page (header + footer + page body all together)
  setLanguage(savedLang);

  // Wire up the language switcher dropdown in the header
  const langSels = document.querySelectorAll('.lang-switcher');
  if (langSels.length > 0) {
    langSels.forEach(sel => {
      sel.value = savedLang;
      sel.addEventListener('change', e => {
        const newLang = e.target.value;
        localStorage.setItem('aimhop-lang', newLang);
        langSels.forEach(s => s.value = newLang); // Sync all dropdowns
        setLanguage(newLang);
      });
    });
  }

  // Active navigation link highlighting
  const currentFileName = pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.main-nav a').forEach(a => {
    const href = a.getAttribute('href');
    if (href && (href === currentFileName || href.endsWith(currentFileName))) {
      a.classList.add('active');
      const parentDropdown = a.closest('.has-dropdown');
      if (parentDropdown) parentDropdown.querySelector(':scope>a')?.classList.add('active');
    }
  });
}

// Run as early as possible (DOMContentLoaded or immediately if already ready)
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initComps);
} else {
  initComps();
}


